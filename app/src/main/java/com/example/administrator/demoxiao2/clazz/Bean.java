public class Bean  {

    private int img;
    private String mane;

    public Bean(int img, String mane) {
        this.img = img;
        this.mane = mane;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getMane() {
        return mane;
    }

    public void setMane(String mane) {
        this.mane = mane;
    }
}
